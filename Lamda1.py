#Lamda is anonymous function 
# one line statement
#functions in the lamda 
#faster
#lamda arguments: expression 


s1 = 'Hello Good Morning'
s2 = lambda func: func.upper()
print(s2(s1))


#if elif else 


n = lambda x: "Positive" if x > 0 else "Negative" if x < 0 else "Zero"
print(n(5))   
print(n(-3))  
print(n(0)) 





